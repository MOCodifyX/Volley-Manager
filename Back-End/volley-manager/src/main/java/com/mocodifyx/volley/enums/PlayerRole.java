package com.mocodifyx.volley.enums;

public enum PlayerRole {
    SETTER,          // Levantador: responsável por organizar as jogadas e distribuir os passes
    OUTSIDE_HITTER,  // Ponta: atacante que também ajuda na defesa e recepção
    LIBERO,          // Líbero: especialista em defesa, não pode atacar nem sacar
    MIDDLE_BLOCKER,  // Central: principal bloqueador e atacante rápido no meio da rede
    OPPOSITE         // Oposto: atacante principal, atua geralmente no ataque e saque
}

